print("For Loop:")
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

print()  

print("For Loop with Range:")
for i in range(5): 
    print(i)

print() 

print("While Loop:")
count = 0
while count < 5:
    print(count)
    count += 1 

print()  

print("Emulating Do-While Loop:")
while True:
    print("This will always run at least once.")
    break  
